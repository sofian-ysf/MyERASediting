// Periodic background sync for checking review status
self.addEventListener('periodicsync', (event) => {
  if (event.tag === 'check-review-status') {
    event.waitUntil(checkReviewStatus());
  }
});

// Check review status
async function checkReviewStatus() {
  const cache = await caches.open(RUNTIME_CACHE);
  const response = await fetch('/api/review/status');
  await cache.put('/api/review/status', response);
}